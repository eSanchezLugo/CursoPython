primerNumero = str(input("Ingresa el primer número, por favor : "))
segundoNumero = str(input("Ingresa el segundo número, por favor : "))

if primerNumero < segundoNumero :
    print(" El primer  número es menor : " + primerNumero)
elif segundoNumero < primerNumero:
    print("El segundo número es menor : " + segundoNumero)    
else:
    print("Los dos números son iguales")
    

